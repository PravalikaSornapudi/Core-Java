public class Operators {
    public static void main(String[] args) {
        
        int x = 10;
        int y = 2;
        int z;

        z = x+y;
        z = x-y;
        z = x*y;
        z = x/y;
        z = x%y;

        System.out.println(z);

        //Order [P-E-M-D-A-S]

        double a = 3+4*(7-5)/2.0;
        System.out.println(a); 
    }
}
